void asym_unit_callback(void * param, void * buf, int result)
{
	struct core_conf_s * conf = param;
	struct asym_unit_test_desc_s * test = conf->test;
	int p;

	(void) buf;

	/* Store result */
	test->retval = result;
	if (result != FPN_CRYPTO(SUCCESS)) {
		test->done = -E_ERR;
	} else {
		/* Compare crypto result */
		if (test->done >= 0) {
			for (p=test->iparams ; p<test->iparams+test->oparams ; p++) {
				if (!memcmp(test->param[p].ptr,
				            test->param[p+test->oparams].ptr,
				            (test->param[p].nbits + 7) / 8)) {
					test->done = 1;
				} else {
					test->done = -E_INVAL;
				}
			}
		}
	}

	/* Free allocated buffers */
	for (p=test->iparams ; p<test->iparams+test->oparams ; p++) {
		if (test->param[p].ptr) free(test->param[p].ptr);
	}

	/* Start next unit test */
	fpn_crypto_init_asym_unit_per_core(conf);
}


/* main processing loop */
int fpn_crypto_init_asym_unit_per_core(void * param)
{
	struct core_conf_s * conf = (struct core_conf_s *) param;
	asym_unit_test_desc_t * test;
	fpn_crypto_kop_t operation;
	int pending = 0;
	int res;
	int p;

	while (!pending) {
		/* Get mutex */
		fpn_spinlock_lock(&mutex);

		/* Get next available unit test */
		conf->test = test = &asym_unit_tests[unit_index];

		/* If no more tests, exit */
		if (unit_index == asym_unit_tests_num) {
			/* Release mutex */
			fpn_spinlock_unlock(&mutex);

			/* Exit */
			return(0);
		}

		/* Step to next test */
		unit_index++;

		/* Release mutex */
		fpn_spinlock_unlock(&mutex);

		/* Not a real test, go to next */
		if (test->param == NULL) {
			continue;
		}

		/* Allocate memory for output */
		for (p=test->iparams ; p<test->iparams+test->oparams ; p++) {
			test->param[p].ptr = malloc((test->param[p].nbits + 7) / 8);
		}

		/* Initialize operation */
		operation.op      = test->op;
		operation.iparams = test->iparams;
		operation.oparams = test->oparams;
		for (p=0 ; p<test->iparams+test->oparams ; p++) {
			operation.param[p].nbits = test->param[p].nbits;
			operation.param[p].ptr   = test->param[p].ptr;
		}
		operation.opaque  = conf;
		operation.cb      = asym_unit_callback;

		while ((res = fpn_crypto_kinvoke(&operation)) == FPN_CRYPTO(BUSY));
		if (res < 0) {
			test->done = -E_UNSUP;

			/* Free allocated buffers */
			for (p=test->iparams ; p<test->iparams+test->oparams ; p++) {
				if (test->param[p].ptr) free(test->param[p].ptr);
			}
		} else {
			/* Operation pending */
			pending = 1;
		}
	}

	return(0);
}

